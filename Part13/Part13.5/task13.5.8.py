def is_correct_bracket(text):
    counter1 = 0
    counter2 = 0
    new_text = "".join(text)
    for char in new_text:
        if char == "(":
            counter1 += 1
        elif char == ")":
            counter2 += 1
        if counter2 > counter1:
            return False
    return not new_text.isalnum() and counter1 == counter2


